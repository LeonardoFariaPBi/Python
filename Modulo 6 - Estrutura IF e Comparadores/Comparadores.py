pagamento = int(input("Digite o primeiro valor: "))
recebimento = int(input("Digite o segundo valor: "))

if pagamento == recebimento:
    print("Este numero eh Igual")
else:
    print("Nao eh igual")
        
if pagamento != recebimento:
   print("Este numero eh Diferente")
else:
   print("Nao eh diferente")

if pagamento > recebimento:
   print("Este numero eh Maior")
else:
   print("Nao eh Maior")

if pagamento < recebimento:
   print("Este numero eh Menor")
else:
   print("Nao eh Menor")

if pagamento >= recebimento:
   print("Este numero eh Maior ou Igual")
else:
   print("Nao eh Maior ou Igual")

if pagamento <= recebimento:
   print("Este numero eh Menor ou Igual")
else:
   print("Nao eh Menor ou Igual")
